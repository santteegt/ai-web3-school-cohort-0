/**
 * DEV Agent A2A Server (Steps 2 + 3) — powered by @a2a-js/sdk
 *
 * Uses the official A2A SDK's AgentExecutor + DefaultRequestHandler pattern.
 * Express middleware provided by @a2a-js/sdk/server/express.
 *
 * Task flow:
 *   First message  → generate hello_world.py → attest via EAS → return artifact (Task)
 *   Accepted message → return review request (direct Message)
 */

import express from 'express';
import { v4 as uuidv4 } from 'uuid';
import { ethers } from 'ethers';
import { AgentCard, AGENT_CARD_PATH, Message, Task, TaskArtifactUpdateEvent, TaskStatusUpdateEvent, TextPart } from '@a2a-js/sdk';
import { AgentExecutor, RequestContext, ExecutionEventBus, DefaultRequestHandler, InMemoryTaskStore } from '@a2a-js/sdk/server';
import { agentCardHandler, jsonRpcHandler, UserBuilder } from '@a2a-js/sdk/server/express';
import { DEV_PORT, DEV_SERVER_URL, DEV_ADDRESS, log } from './config.js';
import { attestDeliverable } from './attest-eas.js';
import { saveState } from './state.js';

const DEV_AGENT_CARD: AgentCard = {
  name: 'DEV Agent',
  description: 'Generates hello_world.py and attests the deliverable hash via EAS on Base Sepolia.',
  protocolVersion: '0.3.0',
  version: '1.0.0',
  url: `${DEV_SERVER_URL}/a2a/jsonrpc`,
  preferredTransport: 'JSONRPC',
  additionalInterfaces: [
    {
      transport: 'JSONRPC',
      url: '${DEV_SERVER_URL}/a2a/jsonrpc',
    }
  ],
  skills: [
    {
      id: 'write_hello_world',
      name: 'Write Hello World',
      description: 'Generates hello_world.py and attests hash on-chain via EAS.',
      tags: ['code', 'python'],
      inputModes: ['text'],
      outputModes: ['text'],
    },
  ],
  capabilities: {
    extensions: [],
    pushNotifications: false,
    streaming: false,
  },
  defaultInputModes: ['text'],
  defaultOutputModes: ['text'],
};

class DevAgentExecutor implements AgentExecutor {
  async execute(requestContext: RequestContext, eventBus: ExecutionEventBus): Promise<void> {
    const { taskId, contextId, userMessage } = requestContext;
    const meta = (userMessage.metadata ?? {}) as Record<string, string>;
    const msgText = userMessage.parts
      .filter((p): p is TextPart => p.kind === 'text')
      .map((p) => p.text)
      .join('\n')
      .trim();

    const isAcceptance = meta.status === 'accepted' || msgText.toLowerCase().includes('accept');

    if (isAcceptance) {
      // Step 5: CLIENT accepted — DEV sends review request as a direct Message
      log('DEV-SERVER', 'Step 5: Received acceptance from CLIENT — sending review request');
      const reviewMsg: Message = {
        kind: 'message',
        messageId: uuidv4(),
        role: 'agent',
        contextId,
        parts: [{
          kind: 'text',
          text: `Thank you for accepting the deliverable! Please provide your feedback and reputation score for this delivery.
            Your review will be recorded on ERC-8004 via giveFeedback(). Context: ${contextId}
          `,
        }],
      };
      eventBus.publish(reviewMsg);
      eventBus.finished();
      return;
    }

    // Steps 2+3: New task — generate hello_world.py and attest via EAS
    const a2aTaskId = meta.task_id ?? taskId;
    log('DEV-SERVER', `Step 2+3: New task — "${msgText.slice(0, 60)}"`);
    log('DEV-SERVER', `Context: ${contextId}  metadata: ${JSON.stringify(meta)}`);

    // Publish initial task so SDK tracks it
    const initialTask: Task = {
      kind: 'task',
      id: taskId,
      contextId,
      status: { state: 'submitted', timestamp: new Date().toISOString() },
      history: [userMessage],
    };
    eventBus.publish(initialTask);

    // Generate hello_world.py
    const content = `#!/usr/bin/env python3
      # Generated by DEV Agent | A2A task: ${a2aTaskId}
      # DEV Agent: ${DEV_ADDRESS}
      print("Hello, World!")
    `;
    const deliverableHash = ethers.keccak256(ethers.toUtf8Bytes(content));
    log('DEV-SERVER', `Deliverable hash (keccak256): ${deliverableHash}`);

    // Step 3: Attest via EAS (blocks until tx confirms)
    const { schemaUID, attestationUID } = await attestDeliverable(deliverableHash, a2aTaskId);

    // Save shared state for demo.ts logging
    saveState({ a2aTaskId, a2aContextId: contextId, deliverableHash });

    log('DEV-SERVER', '✅ Task completed — artifact with EAS UID delivered');

    // Publish artifact with EAS UID embedded (the critical three-standards linkage)
    const artifactUpdate: TaskArtifactUpdateEvent = {
      kind: 'artifact-update',
      taskId,
      contextId,
      artifact: {
        artifactId: 'hello-world',
        name: 'hello_world.py',
        parts: [
          { kind: 'text', text: content },
          {
            kind: 'data',
            data: {
              deliverable_hash: deliverableHash,
              eas_attestation_uid: attestationUID,
              eas_schema_uid: schemaUID,
            },
          },
        ],
      },
    };
    eventBus.publish(artifactUpdate);

    const finalUpdate: TaskStatusUpdateEvent = {
      kind: 'status-update',
      taskId,
      contextId,
      status: { state: 'completed', timestamp: new Date().toISOString() },
      final: true,
    };
    eventBus.publish(finalUpdate);
    eventBus.finished();
  }

  cancelTask = async (): Promise<void> => {};
}

export async function startDevServer(): Promise<ReturnType<typeof express.application.listen>> {
  const executor = new DevAgentExecutor();
  const requestHandler = new DefaultRequestHandler(
    DEV_AGENT_CARD,
    new InMemoryTaskStore(),
    executor,
  );

  const app = express();
  app.use(`/${AGENT_CARD_PATH}`, agentCardHandler({ agentCardProvider: requestHandler }));
  app.use('/a2a/jsonrpc', jsonRpcHandler({ requestHandler, userBuilder: UserBuilder.noAuthentication }));

  return new Promise((resolve) => {
    const server = app.listen(DEV_PORT, () => {
      log('DEV-SERVER', `A2A server (SDK) listening at ${DEV_SERVER_URL}`);
      log('DEV-SERVER', `Agent card: ${DEV_SERVER_URL}/.well-known/agent-card.json`);
      resolve(server);
    });
  });
}

// Allow direct execution: pnpm dev:server
if (import.meta.url === `file://${process.argv[1]}`) {
  await startDevServer();
}
